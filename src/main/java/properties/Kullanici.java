package properties;

public class Kullanici {
	private String kID; 
	private String kAdi; 
	private String kSoyadi; 
	private String kEmail; 
	private String kTelefon; 
	private String kDogumTarihi; 
	private String kSifre;
	
	public String getkID() {
		return kID;
	}
	public void setkID(String kID) {
		this.kID = kID;
	}
	public String getkAdi() {
		return kAdi;
	}
	public void setkAdi(String kAdi) {
		this.kAdi = kAdi;
	}
	public String getkSoyadi() {
		return kSoyadi;
	}
	public void setkSoyadi(String kSoyadi) {
		this.kSoyadi = kSoyadi;
	}
	public String getkEmail() {
		return kEmail;
	}
	public void setkEmail(String kEmail) {
		this.kEmail = kEmail;
	}
	public String getkTelefon() {
		return kTelefon;
	}
	public void setkTelefon(String kTelefon) {
		this.kTelefon = kTelefon;
	}
	public String getkDogumTarihi() {
		return kDogumTarihi;
	}
	public void setkDogumTarihi(String kDogumTarihi) {
		this.kDogumTarihi = kDogumTarihi;
	}
	public String getkSifre() {
		return kSifre;
	}
	public void setkSifre(String kSifre) {
		this.kSifre = kSifre;
	} 
}
